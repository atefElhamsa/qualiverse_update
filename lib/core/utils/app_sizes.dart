// This class defines constant values for application dimensions and padding.
class AppSizes {
  // Defines the standard width of the application.
  static const double appWidth = 1280;

  // Defines the standard height of the application.
  static const double appHeight = 832;

  // Defines the standard top padding for application content.
  static const double appPaddingTop = 12;

  // Defines the standard bottom padding for application content.
  static const double appPaddingBottom = 21;

  // Defines the standard horizontal (left and right) padding for application content.
  static const double appPaddingLeftAndRight = 5;
}
